i1 = int(input("Enter an integer "))
i2 = int(input("Enter another integer "))
o = input("Enter the operation to be used ")
if o == '+':
    print(i1+i2)
elif o == '-':
    print(i1-i2)
elif o == '*':
    print(i1*i2)
elif o == '/':
    print(i1/i2)
elif o == '%':
    print(i1%i2)
elif o == '^':
    print(i1**i2)